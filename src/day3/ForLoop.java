package day3;

public class ForLoop 
{

	public static void main(String[] args)
	{
		System.out.println("ascending order no 0-10");
		int i;
		for(i=0;i<=10;i++) //also define data type for(int i=0;....;...)
		{
			System.out.println(i);
		}
		System.out.println("descending order no from 10-0");
		for(int j=10;j>=0;j--)
		{
			System.out.println(j);
		}
	}

}
